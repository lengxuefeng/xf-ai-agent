# -*- coding: utf-8 -*-
"""
Agent 侧通用提示文案常量。

这些常量定义了Agent在执行过程中可能触发的各种错误和中断情况的提示文案。
这些文案会直接显示给用户，因此需要友好、清晰、可操作。

设计原则：
1. 错误提示要明确告知用户发生了什么
2. 提供下一步应该怎么做的建议
3. 避免技术术语，使用用户能理解的语言
4. 对于需要人工介入的情况，说明等待什么动作
"""

# 当搜索Agent因为循环次数过多而停止时的提示
# 场景：用户问了一个非常模糊的问题，导致Agent一直在搜索不同的关键词但找不到有用信息
SEARCH_TOOL_LOOP_EXCEEDED_MESSAGE = "联网检索调用次数过多，已自动停止以避免卡住。请换个关键词重试。"

# 当用户在天气对话中突然切换到搜索其他信息时的拦截提示
# 场景：用户本来在问天气，突然问"附近有啥好玩的地方"，Agent误以为是天气查询
SEARCH_TOPIC_DRIFT_BLOCK_MESSAGE = (
    "我理解你现在问的是小区/房价位置信息，不是天气。"
    "我将按该主题继续检索。若需要天气，请明确说明'查天气'。"
)

# 当搜索工具调用失败时的快速收尾提示
# 场景：网络问题、API限流、服务宕机等导致无法进行搜索
SEARCH_TOOL_FAILURE_MESSAGE = (
    "联网检索当前不可用或超时，我已停止重复调用以避免卡住。"
    "请稍后重试，或把问题拆成更短关键词后再试。"
)

# 当天气工具因为循环次数过多而停止时的提示
# 场景：用户问了一个关于多个城市天气的复杂问题，导致Agent一直在循环调用
WEATHER_TOOL_LOOP_EXCEEDED_MESSAGE = "天气工具调用次数过多，已停止当前流程。请稍后重试。"

# 当云柚Agent检测到需要审批的操作时的中断提示
# 场景：用户要求删除Holter设备记录，这是一个敏感操作，需要人工审批
YUNYOU_REVIEW_INTERRUPT_MESSAGE = "检测到敏感操作，请审核。"

# 云柚敏感操作的详细描述，会显示在审批界面
# 场景：前端需要显示一个清晰的审批描述，让用户知道他在审批什么
YUNYOU_REVIEW_DESCRIPTION = "⚠️ 敏感业务数据操作，需审批。"

# 当GraphRunner拒绝某个操作时的提示消息
# 场景：审批被拒绝后，需要给用户一个明确的反馈
GRAPH_RUNNER_REJECTED_MESSAGE = "已拒绝本次敏感操作。"
