# -*- coding: utf-8 -*-
"""
SSE 事件类型与统一提示文案。

Server-Sent Events (SSE) 是前端和后端之间实时通信的协议。
后端通过SSE向前端推送事件流，前端可以实时显示Agent的思考过程、执行状态等。

事件流格式：
event: <事件类型>
data: <JSON格式的数据>

设计要点：
1. 事件类型要明确分类，前端可以根据类型做不同的UI展示
2. 数据结构要统一，方便前端解析
3. 提示文案要友好，让用户知道发生了什么
4. 错误处理要完善，给用户明确的反馈
"""
from enum import Enum


class SseEventType(str, Enum):
    """
    SSE 事件类型枚举。

    定义了后端可能向前端推送的所有事件类型。
    前端根据这些类型来决定如何渲染不同的UI组件（如思考气泡、流式文本、审批按钮等）。
    """

    # 响应开始事件：表示一次完整的对话响应开始了
    # 前端可以用这个事件清空之前的显示状态，准备展示新的内容
    RESPONSE_START = "response_start"

    # 响应结束事件：表示这次响应已经完全结束
    # 前端可以用这个事件停止加载动画，显示完成状态
    RESPONSE_END = "response_end"

    # 思考过程事件：表示Agent正在思考，内容是思考的具体内容
    # 前端可以将这些内容展示为"思考气泡"，让用户知道Agent在做什么
    THINKING = "thinking"

    # 流式内容事件：这是最重要的事件，承载实际的对话内容
    # 前端需要不断接收这些事件并拼接成完整的回复
    STREAM = "stream"

    # 工具调用事件：用于把工具名、参数和状态从正文流中剥离出来
    # 前端应以独立状态栏或时间线展示，而不是混入 Markdown 文本
    TOOL_CALL = "tool_call"

    # 错误事件：表示执行过程中发生了错误
    # 前端需要展示错误提示，并决定是重试还是显示给用户
    ERROR = "error"

    # 中断审批事件：表示Agent执行到了需要人工审批的敏感操作
    # 前端需要显示审批界面，让用户选择批准或拒绝
    INTERRUPT = "interrupt"

    # 日志事件：用于调试和排错，记录执行过程中的关键信息
    # 生产环境可以不展示给用户，但需要收集到日志系统
    LOG = "log"

    # 结构化工作流事件：携带Agent执行流程的结构化信息
    # 前端可以用这些信息展示执行流程图、时间线等可视化内容
    WORKFLOW_EVENT = "workflow_event"


class SsePayloadField(str, Enum):
    """
    SSE 负载字段名枚举。

    定义SSE事件数据中的标准字段名。
    统一字段名可以避免前后端因为字段名不一致导致的问题。
    """

    # 事件类型字段：在data内部也存储type，方便解析
    TYPE = "type"

    # 事件内容字段：存储实际的文本内容
    CONTENT = "content"

    # 结构化负载字段：存储复杂的数据结构（如工作流事件中的详细信息）
    PAYLOAD = "payload"


class SseMessage:
    """
    GraphRunner 常用提示文案常量。

    这些文案会在不同场景下通过SSE推送给前端。
    文案设计原则：清晰、友好、可操作。
    """

    # 检测到审批恢复操作
    # 场景：用户在前端点击了批准/拒绝按钮，后端检测到并准备恢复执行
    RESUME_DETECTED = "检测到审批结果，正在恢复执行..."

    # 恢复参数无效
    # 场景：前端传递的resume参数有问题，可能是session_id或message_id错误
    INVALID_RESUME_PARAM = "参数无效或无待恢复任务"

    # 处理中心跳提示
    # 场景：长时间没有新的SSE事件，避免用户以为系统卡死
    PROCESSING_HEARTBEAT = "系统正在处理中，请稍候..."

    # 超时错误提示
    # 场景：模型服务响应时间超过配置的超时阈值
    ERROR_TIMEOUT = "处理超时，请稍后重试。"

    # 连接异常错误提示
    # 场景：无法连接到模型服务，可能是网络问题或服务宕机
    ERROR_CONNECTION = "模型服务连接异常，请稍后重试。"

    # 运行期错误提示
    # 场景：代码执行、数据库操作等底层服务出现异常
    ERROR_RUNTIME = "底层服务执行异常，请稍后重试。"

    # 空闲超时错误提示
    # 场景：建立了连接但长时间没有收到任何响应
    ERROR_IDLE_TIMEOUT = "长时间未收到模型响应，请重试。"

    # 任务中断错误提示
    # 场景：执行过程中被取消或强制停止
    ERROR_TASK_INTERRUPTED = "任务已中断，请重试。"

    # 需要人工审批提示
    # 场景：Agent执行到敏感操作，触发中断，需要用户决策
    NEED_MANUAL_APPROVAL = "检测到需要人工审核，请点击'批准/拒绝'后继续。"

    # 恢复后仍需审批提示
    # 场景：复杂的审批场景可能需要多轮审批
    RESUME_NEED_MANUAL_APPROVAL = "恢复执行后仍需人工审核，请继续审批。"

    # 未找到中断任务错误
    # 场景：用户点击了恢复按钮，但后端找不到对应的待审批任务
    ERROR_NO_INTERRUPTED_TASK = "未找到处于中断状态的子任务"

    # 恢复无结果错误
    # 场景：审批恢复流程执行完毕，但没有生成任何结果
    ERROR_RESUME_EMPTY_RESULT = "恢复执行未生成结果，请重试。"


class SseContentType:
    """
    用于正文提取的消息类型常量。

    在解析SSE事件时，需要区分哪些事件包含实际的对话内容。
    这些类型的事件需要被提取出来，拼接成完整的AI回复。
    """

    # 流式内容类型：直接拼接的文本内容
    STREAM = SseEventType.STREAM.value

    # 消息内容类型：完整的一条消息（可能包含结构化信息）
    MESSAGE = "message"
