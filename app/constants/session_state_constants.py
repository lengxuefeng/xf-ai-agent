# -*- coding: utf-8 -*-
"""
会话状态（Session State）相关常量。

定义了会话状态管理中需要用到的正则表达式、关键词列表等配置。
会话状态管理是系统"记忆"用户信息和对话上下文的核心模块。

设计要点：
1. 城市提取要准确，避免误识别通用词
2. 关键词匹配要全面，覆盖常见的自然表达
3. 槽位更新要合理，避免频繁覆盖
4. 上下文长度要控制，避免token浪费
"""

# 城市提取正则：匹配中文行政区地名（如"郑州""郑州市""金水区"）
# 正则说明：
# - [\u4e00-\u9fa5]{2,8} 匹配2-8个中文字符
# - (?:市|县|区|州|盟|旗)? 匹配可选的行政区划后缀
# 场景：用户说"我在郑州"、"郑州的天气怎么样"都能识别出"郑州"
SESSION_CITY_REGEX = r"([\u4e00-\u9fa5]{2,8}(?:市|县|区|州|盟、旗)?)"

# 口语后缀清理：用于处理"郑州的呀""上海呢"这类输入
# 用户经常会使用语气词和助词，这些词在语义上不影响实体识别，但会影响匹配
# 场景：用户说"郑州的呀"，清理后变为"郑州"，便于后续处理
SESSION_SUFFIX_PARTICLE_REGEX = r"[的了呀啊呢吧吗嘛～~\s,，。！!？?]+"

# 可能导致误识别的通用词，命中后不作为城市写入槽位
# 这些词在口语中经常出现，但并不是真实的城市名称
# 场景：用户说"这个城市怎么样"，不应提取"城市"作为城市槽位
SESSION_CITY_STOPWORDS = (
    "城市",   # 通用词
    "附近",   # 相对位置描述
    "哪里",   # 疑问词
    "什么",   # 疑问词
    "怎么",   # 疑问词
    "为什么", # 疑问词
    "好玩",   # 描述词
    "天气",   # 主题词
    "活动",   # 主题词
    "房价",   # 主题词
    "小区",   # 通用名词
    "推荐",   # 动词
)

# 本地推荐/周边检索关键词（通常依赖城市槽位）
# 这些关键词通常和地点相关，需要配合城市信息才能给出准确的推荐
# 场景：用户说"附近有什么好吃的"，需要先知道用户在哪个城市
SESSION_LOCAL_DISCOVERY_KEYWORDS = (
    "附近",   # 附近地点
    "周边",   # 周边环境
    "好玩",   # 娱乐场所
    "去哪玩", # 旅游推荐
    "推荐",   # 推荐
    "商场",   # 商业场所
    "影院",   # 文化娱乐
    "景点",   # 旅游景点
    "美食",   # 餐饮
    "活动",   # 活动/事件
)

# 主题词典：用于维护会话的高层 topic 槽位，避免上下文漂移
# 通过识别对话中的主题关键词，可以更好地理解用户意图和上下文
# 场景：用户在问天气，突然问附近有什么美食，可以识别到话题切换
SESSION_TOPIC_KEYWORDS = {
    # 天气相关关键词
    "weather": ("天气", "气温", "下雨", "空气质量", "湿度", "风力"),

    # 搜索相关关键词
    "search": ("搜索", "活动", "新闻", "附近", "推荐", "好玩", "周边"),

    # 数据库相关关键词
    "sql": ("sql", "数据库", "数据表", "order by", "limit", "查询语句"),

    # 云柚医疗设备相关关键词
    "holter": ("holter", "云柚", "动态心电", "贴片", "报告状态"),
}

# 会话中最多保留的关键事实条数，避免上下文无限膨胀
# 关键事实包括用户的需求、约束、偏好等信息
# 场景：用户在对话中多次提到不同的需求，只保留最近的10条，避免token浪费
SESSION_MAX_KEY_FACTS = 10
